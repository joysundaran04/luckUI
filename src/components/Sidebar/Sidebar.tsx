interface SidebarProps {
    onLogout: () => void;
    activeItem: string;
    setActiveItem: (item: string) => void;
}

const Sidebar: React.FC<SidebarProps> = ({ onLogout, activeItem, setActiveItem }) => {
    const [collapsed, setCollapsed] = useState(false);
    const isAdmin = UserService.isAdmin();

    const allMenuItems = [
        { id: 'dashboard', label: 'Dashboard', icon: icons.dashboard, adminOnly: true },
        { id: 'book', label: 'Books', icon: icons.book, adminOnly: false },
        { id: 'agent', label: 'Agents', icon: icons.agent, adminOnly: false },
        { id: 'users', label: 'Users', icon: icons.users, adminOnly: true },
        { id: 'winners', label: 'Winners', icon: icons.winners, adminOnly: false },
    ];

    const menuItems = allMenuItems.filter(item => !item.adminOnly || isAdmin);

    return (
        <aside className={`sidebar ${collapsed ? 'collapsed' : ''}`}>
            <div className="sidebar-header">
                <div className="logo-container">
                    <div className="logo-icon">✨</div>
                    {!collapsed && <span className="logo-text">LuckyDraw</span>}
                </div>
                <button
                    className="toggle-btn"
                    onClick={() => setCollapsed(!collapsed)}
                    aria-label="Toggle Sidebar"
                >
                    {icons.menu}
                </button>
            </div>

            <nav className="sidebar-nav">
                <ul>
                    {menuItems.map(item => (
                        <li key={item.id} className={activeItem === item.id ? 'active' : ''}>
                            <button onClick={() => setActiveItem(item.id)}>
                                <span className="icon">{item.icon}</span>
                                {!collapsed && <span className="label">{item.label}</span>}
                            </button>
                        </li>
                    ))}
                </ul>
            </nav>

            <div className="sidebar-footer">
                <button className="logout-btn" onClick={onLogout}>
                    <span className="icon">{icons.logout}</span>
                    {!collapsed && <span className="label">Logout</span>}
                </button>
            </div>
        </aside>
    );
};

export default Sidebar;
